function love.conf(t)
    t.console = true
    t.window.title = "Game Server"
    -- t.console = false -- Disables the console
    t.audio = false   -- Disables the audio module
    t.window = false  -- Disables the window module
    t.keyboard = false-- Disables keyboard input
    t.mouse = false   -- Disables mouse input
    t.joystick = false -- Disables joystick input
    t.timer = false   -- Disables the timer module
end